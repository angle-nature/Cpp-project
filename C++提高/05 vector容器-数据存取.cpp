#include<iostream>
using namespace std;
#include<vector>

//vector容器的数据存取操作
void test()
{
	vector<int>v1;

	//尾插
	for (int i = 0; i < 10; i++)
	{
		v1.push_back(i);
	}

	//利用[]的方式访问单个元素
	for (int i = 0; i < v1.size(); i++)
	{
		cout << v1[i] << " ";
	}
	cout << endl;

	//利用at方式访问单个元素
	for (int i = 0; i < v1.size(); i++)
	{
		cout << v1.at(i) << " ";
	}
	cout << endl;

	//获取第2个元素
	cout << "第一个元素：" << v1.front()+1 << endl;

	//获取最后一个元素
	cout << "最后一个元素：" << v1.back() << endl;
}

int main()
{
	test();

	system("pause");

	return 0;
}