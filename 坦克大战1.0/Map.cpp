void Map::loadMap()
{
	IMAGE brick1;
	IMAGE brick2;
	IMAGE home;
	loadimage(&home, _T("home.jpg"), 50, 50); //基地
	loadimage(&brick1, _T("wall1.jpg"), 25, 25);//可摧毁墙
	loadimage(&brick2, _T("wall2.jpg"), 25, 25);//不可摧毁墙
	for (int i = 0; i < 26; i++)
	{
		for (int j = 0; j < 26; j++)
		{
			switch (map[i][j])
			{
			case 1:putimage(25 * j, 25 * i, &brick1); break;
			case 2:putimage(25 * j, 25 * i, &brick2); break;
			case 3:
				putimage(25 * j, 25 * i, &home);
				map[i][j] = 4;
				break;
			}
		}
	}
}

